import { AxiosResponse } from 'axios';
import type { InferGetServerSidePropsType, NextPage } from 'next';
import Head from 'next/head';
import { getPlaiceholder } from 'plaiceholder';
import { useState } from 'react';
import ReactPageScroller from 'react-page-scroller';
import { IAbout, ISlider, IStudio } from 'server/interface';
import instance from 'src/api/httpService';
import Header from 'src/components/Common/Header';
import Contact from 'src/components/Contact';
import About from 'src/components/Info/about';
import ProjectItem from 'src/components/ProjectItem';
import Slider from 'src/components/Slider';
import { config } from 'src/config';

const Home: NextPage<
    InferGetServerSidePropsType<typeof getServerSideProps>
> = ({ sliderImages, about, studios }) => {

    const [currentPage, setCurrentPage] = useState(0);
    const handlePageChange = (pageNumber: number) => {
        setCurrentPage(pageNumber);
    };

    return (
        <>
            <Head>
                <title>Chinta</title>
                <meta
                    name="description"
                    content="Generated by create next app"
                />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <Header />
            <main className="section-wrapper">
                <ReactPageScroller
                    pageOnChange={handlePageChange}
                    customPageNumber={currentPage}
                >
                    <Slider sliderImages={sliderImages} />
                    <div className="section ">
                        <div className="container d-flex align-items-center justify-content-center h-100">
                            <About about={about} />
                        </div>

                    </div>
                    <div className="section ">
                        <div className="container ">
                            <div className="projects">
                                <div className="row g-2 g-sm-3  row-cols-2 row-cols-sm-3 row-cols-md-4 row-cols-lg-5 ">
                                    {Array(30)
                                        .fill('_')
                                        .map((item, index) => (
                                            <ProjectItem key={index + 1} />
                                        ))}
                                </div>
                            </div>
                        </div>
                    </div>
                    <div className="section">
                        <Contact />
                    </div>
                </ReactPageScroller>





            </main>
        </>
    );
};

export const getServerSideProps = async () => {
    /** Slider Data Handler */
    const { data: sliders } = await instance.get<AxiosResponse<ISlider[]>>(
        '/sliders'
    );

    const { data: studio } = await instance.get<AxiosResponse<IStudio[]>>(
        '/info/studios'
    );

    const { data: _about } = await instance.get<{ data: IAbout[]; }>(
        '/info/about'
    );

    const sliderImages = await Promise.all(
        sliders.data.map(async (data) => {
            const { base64, img } = await getPlaiceholder(`${config.imageUploadUrl}${data.photoUrl}`);
            return {
                ...img,
                ...data,
                base64: base64,
            };
        })
    );

    const aboutData = _about.data[0];
    const about = await getPlaiceholder(`${config.imageUploadUrl}${aboutData.photoUrl}`).then(
        ({ base64, img }) => {
            return {
                ...img,
                ...aboutData,
                base64: base64,
            };
        }
    );

    const studios = await Promise.all(
        studio.data.map(async (data) => {
            const { base64, img } = await getPlaiceholder(`${config.imageUploadUrl}${data.photoUrl}`);
            return {
                ...img,
                ...data,
                base64: base64,
            };
        })
    );
    return {
        props: {
            sliderImages,
            about,
            studios,
        },
    };

};

export default Home;
