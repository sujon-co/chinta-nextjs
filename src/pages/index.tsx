export const getStaticProps = async () => {
    const images = [
        {
            image: '/sliders/1.jpg',
            alt: 'alternative data',
        },
        {
            image: '/sliders/2.jpg',
            alt: 'alternative data',
        },
        {
            image: '/sliders/3.jpg',
            alt: 'alternative data',
        },
        {
            image: '/sliders/4.jpg',
            alt: 'alternative data',
        },
        {
            image: '/sliders/5.jpg',
            alt: 'alternative data',
        },
        {
            image: '/sliders/6.jpg',
            alt: 'alternative data',
        },
        {
            image: '/sliders/7.jpg',
            alt: 'alternative data',
        },
        {
            image: '/sliders/8.jpg',
            alt: 'alternative data',
        },
    ];

    const sliderImages = await Promise.all(
        images.map(async (data) => {
            const { base64, img } = await getPlaiceholder(data.image);
            return {
                ...img,
                base64: base64,
                alt: data.alt,
            };
        })
    ).then((value) => value);

    return {
        props: {
            sliderImages,
        },
    };
};

export default Home;
